import { subDays } from 'date-fns'
import { supabase, setRememberMe } from '../lib/supabase'
import { dateKey, todayKey } from '../lib/format'
import { xpForCompletion } from '../lib/gamification'

/**
 * Supabase repository — the production data path.
 *
 * Implements the same contract as mockRepo. Row Level Security (see supabase/policies.sql)
 * is what actually enforces "you can only read your own data and your partner's"; the
 * queries here assume it and never filter for safety alone.
 */

const unwrap = ({ data, error }) => {
  if (error) throw new Error(error.message)
  return data
}

export const supabaseRepo = {
  mode: 'supabase',

  /* ------------------------------------------------------------------ auth */

  async getSession() {
    const { data } = await supabase.auth.getSession()
    return data.session ?? null
  },

  onAuthStateChange(cb) {
    const {
      data: { subscription },
    } = supabase.auth.onAuthStateChange((_event, session) => cb(session))
    return () => subscription.unsubscribe()
  },

  async signUp({ name, email, password }) {
    // `name` rides along in user_metadata; the handle_new_user() trigger copies it
    // into public.profiles so we never need a second round-trip.
    const data = unwrap(
      await supabase.auth.signUp({
        email,
        password,
        options: { data: { name } },
      }),
    )
    return data.session
  },

  async signIn({ email, password, remember = true }) {
    setRememberMe(remember)
    const data = unwrap(await supabase.auth.signInWithPassword({ email, password }))
    return data.session
  },

  async signOut() {
    const { error } = await supabase.auth.signOut()
    if (error) throw new Error(error.message)
  },

  async resetPassword(email) {
    const { error } = await supabase.auth.resetPasswordForEmail(email, {
      redirectTo: `${window.location.origin}/reset-password`,
    })
    if (error) throw new Error(error.message)
  },

  async updatePassword(password) {
    const { error } = await supabase.auth.updateUser({ password })
    if (error) throw new Error(error.message)
  },

  /** Calls a SECURITY DEFINER function — the client can't delete from auth.users itself. */
  async deleteAccount() {
    const { error } = await supabase.rpc('delete_own_account')
    if (error) throw new Error(error.message)
    await supabase.auth.signOut()
  },

  /* --------------------------------------------------------------- profile */

  async getProfile(userId) {
    return unwrap(await supabase.from('profiles').select('*').eq('id', userId).maybeSingle())
  },

  async updateProfile(userId, patch) {
    return unwrap(
      await supabase.from('profiles').update(patch).eq('id', userId).select().single(),
    )
  },

  /* ---------------------------------------------------------------- couple */

  async getCoupleState(userId) {
    const membership = unwrap(
      await supabase.from('couple_members').select('couple_id').eq('user_id', userId).maybeSingle(),
    )
    if (!membership) return { couple: null, partner: null, members: [] }

    const couple = unwrap(
      await supabase.from('couples').select('*').eq('id', membership.couple_id).single(),
    )
    // RLS lets us see rows for anyone in our couple, so this returns both partners.
    const rows = unwrap(
      await supabase
        .from('couple_members')
        .select('user_id, joined_at, profiles(*)')
        .eq('couple_id', membership.couple_id),
    )

    const members = rows.map((r) => r.profiles).filter(Boolean)
    return {
      couple,
      members,
      partner: members.find((p) => p.id !== userId) ?? null,
    }
  },

  async createCouple(userId) {
    // invite_code is generated by a column default in SQL, so two clients can't collide.
    const couple = unwrap(
      await supabase.from('couples').insert({ created_by: userId }).select().single(),
    )
    unwrap(
      await supabase
        .from('couple_members')
        .insert({ couple_id: couple.id, user_id: userId })
        .select()
        .single(),
    )
    return couple
  },

  async joinCouple(userId, code) {
    const couple = unwrap(
      await supabase
        .from('couples')
        .select('*')
        .eq('invite_code', code.trim().toUpperCase())
        .maybeSingle(),
    )
    if (!couple) throw new Error("We couldn't find that code. Double-check it with your partner?")

    const { error } = await supabase
      .from('couple_members')
      .insert({ couple_id: couple.id, user_id: userId })

    if (error) {
      // The max-2-members trigger raises this; translate it into human language.
      if (/two|full|limit/i.test(error.message)) {
        throw new Error('That couple is already complete — only two people allowed 💜')
      }
      throw new Error(error.message)
    }
    return couple
  },

  async leaveCouple(userId) {
    const { error } = await supabase.from('couple_members').delete().eq('user_id', userId)
    if (error) throw new Error(error.message)
  },

  async updateCouple(coupleId, patch) {
    return unwrap(await supabase.from('couples').update(patch).eq('id', coupleId).select().single())
  },

  /* ----------------------------------------------------------------- goals */

  async listGoals(userId) {
    return unwrap(
      await supabase
        .from('goals')
        .select('*')
        .eq('user_id', userId)
        .order('created_at', { ascending: false }),
    )
  },

  async listCoupleGoals(coupleId) {
    return unwrap(await supabase.from('goals').select('*').eq('couple_id', coupleId))
  },

  async createGoal(goal) {
    return unwrap(await supabase.from('goals').insert(goal).select().single())
  },

  async updateGoal(goalId, patch) {
    return unwrap(await supabase.from('goals').update(patch).eq('id', goalId).select().single())
  },

  async deleteGoal(goalId) {
    const { error } = await supabase.from('goals').delete().eq('id', goalId)
    if (error) throw new Error(error.message)
  },

  /**
   * Single write path for progress. Kept as a transaction on the server (log_progress)
   * so the counter, the progress row, the XP award and the activity feed can never
   * drift apart if the client dies halfway through.
   */
  async setGoalValue({ goalId, date = todayKey(), value }) {
    return unwrap(
      await supabase.rpc('log_progress', {
        p_goal_id: goalId,
        p_date: date,
        p_value: Math.max(0, value),
      }),
    )
  },

  /* -------------------------------------------------------------- progress */

  async listProgress(userId, days = 365) {
    return unwrap(
      await supabase
        .from('goal_progress')
        .select('goal_id, user_id, date, value')
        .eq('user_id', userId)
        .gte('date', dateKey(subDays(new Date(), days)))
        .order('date', { ascending: true }),
    )
  },

  /* -------------------------------------------------------------- activity */

  async listActivity(coupleId, limit = 12) {
    return unwrap(
      await supabase
        .from('activity_feed')
        .select('*')
        .eq('couple_id', coupleId)
        .order('created_at', { ascending: false })
        .limit(limit),
    )
  },

  /* -------------------------------------------------------------- realtime */

  /**
   * One channel for the whole couple. Any insert/update/delete on the shared tables
   * pings `onChange`, which invalidates the React Query cache — so when Riya ticks off
   * a workout, Rachit's rings move without a refresh.
   */
  subscribeToCouple(coupleId, onChange) {
    const channel = supabase
      .channel(`couple:${coupleId}`)
      .on('postgres_changes', { event: '*', schema: 'public', table: 'goals' }, onChange)
      .on('postgres_changes', { event: '*', schema: 'public', table: 'goal_progress' }, onChange)
      .on(
        'postgres_changes',
        { event: '*', schema: 'public', table: 'activity_feed', filter: `couple_id=eq.${coupleId}` },
        onChange,
      )
      .on('postgres_changes', { event: '*', schema: 'public', table: 'profiles' }, onChange)
      .on(
        'postgres_changes',
        { event: '*', schema: 'public', table: 'couple_members', filter: `couple_id=eq.${coupleId}` },
        onChange,
      )
      .subscribe()

    return () => supabase.removeChannel(channel)
  },

  /** XP is awarded server-side by log_progress; exposed only so the contract matches. */
  xpForCompletion,
}

export default supabaseRepo
